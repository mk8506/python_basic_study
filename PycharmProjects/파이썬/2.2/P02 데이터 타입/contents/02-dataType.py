# 자료형
# 정수형(int): 음수, 0, 양수 데이터. Integer
num1 = 10
print(num1, type(num1))
# 실수(float): 소수점을 포함한 숫자 데이터
num2 = 10.0
print(num2, type(num2))
# 문자형(str): 문자열 데이터. String
msg = '10' # 일공
print(msg, type(msg))
# 논리형(bool): 참, 거짓. Boolean
k = True # 꼭 대문자로 시작.
print(k, type(k))
print()

# 같은 변수도 대입값이 바뀌면 타입도 바뀜
var = 20
print('var:', var, type(var))
var = 1.4
print('var:', var, type(var))

